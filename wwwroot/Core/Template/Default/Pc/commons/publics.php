                    <p">在获得自身稳健发展的同时，通威集团始终坚持以一个成功企业的高度社会责任感，情系民生，饮水思源，用真情和爱心回馈社会各界的厚爱，积极参与社会公益事业、光彩事业和思源工程。</p>
                    <p>2007年，通威集团积极参与民建中央发起的“中华思源工程扶贫基金会”活动，独家设立“中华思源工程.阳光计划”。十年时间,通威平均每年投入500万元，累计投入5000万元，用于“中华思源工程.阳光计划”的实施。目前已先后在四川理塘、四川盐源、河北丰宁、新疆阜康等地建成多座太阳能光伏电站，为经济欠发达地区送去了优质、高效、环保的生活用电。此外,通威还多次挺进青藏无人区，向青藏绿色驿站、长江源保护区公益捐赠光伏电站，用绿色清洁能源点亮世界屋脊。
                    </p>
                    <p>奉献社会，责无旁贷。2022年9月，通威集团第一时间捐赠5000万元人民币，支援四川泸定地震灾区紧急救援、过渡安置以及灾后重建工作。迄今为止，通威集团各种捐款捐物总额已超过5亿元人民币。</p>
