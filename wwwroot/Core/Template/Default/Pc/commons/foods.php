                <p style="line-height: 2;">通威食品是通威股份旗下主要涉足水产品和畜禽产品研发、生产和销售的业务板块，为消费者的餐桌提供健康、安全、美味的优质食品。通威食品旗下拥有“通威鱼”、“春源”、“新太丰”、“鱼本家”、“醉牙尖”、“微腊”、“猫无刺”等多个品牌，为确保每一款产品的健康安全，公司全力整合行业资源，按照“三鱼一虾+一平台+一市场”的战略定位有序推进各项工作，在产业链上实现了从养殖、加工到销售的全过程品质监控。通威食品打造的中国知名品牌鱼-“通威鱼”已经实现了全产业链管理，公司在全国建立了多个优质养殖基地，并在成都、海南等地建有产品深加工基地，加工过程严格执行ISO、HACCP管理体系以及欧盟和美国FDA认证标准，每一道生产环节都经过严格检测和控制，从而确保每件产品的卓越品质。
