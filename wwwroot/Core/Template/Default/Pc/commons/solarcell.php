                <p class="title">自主创新，提高光电转化效率</p>
                <div class="state">
                    通威太阳能继续大力推进技术研发和工艺优化，在主流电池技术的提效和电池新技术研发方面均取得阶段性进展。在PERC技术方面,通过对栅线团、陷光结构、扩散和钝化工艺等量产化技术的最优集成，电池效率进一步提升，2020年7月公司166尺寸电池最高转换效率达到 23.47%，创造PERC电池产业化转换效率的世界纪录；在新技术方面， 公司于2019年投产400MW异质结试验线，是行业中较早建成异质结研发线的企业，最高转换效率已达25.67%，量产转换效率达到24.66%。通威太阳能新增建设1GW异质结中试线，目前相关设备已调试完毕，产线处于产能爬坡阶段，公司将围绕异质结规模量产条件下的提效降本目标进行持续研发攻关。通威太阳能于2020年启动了基于210尺寸的TOPCon技术研发，量产转换效率达到24.70%。为进一步加大TOPCon技术在规模量产条件下的研发试验，通威太阳能计划对现有部分PERC产线进行技改提升，为光伏行业下一代量产电池技术的研发和推进做出贡献。

